////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(first, second) {
    if (first > second) {
      return second;
    } else if (second > first) {
      return first;
    } else {
      return first || second
    }
    
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(number) {
    if (number === 0) {
      return true
    } else if (number === 1) {
      return false
    } else if (number === -1) {
      return false
    } else {
      return isEven(number - 2)
    }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, character) {
  let counter = 0;  
  for (let i = 0; i < string.length - 1; i++) {
       if (string[i] === character) { 
       counter += 1
       }      
  } return counter; 
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  let counter = 0;  
  for (let i = 0; i < string.length - 1; i++) {
       if (string[i].toLowerCase() === 'b') { 
       counter += 1
       }     
  } return counter; 
}

