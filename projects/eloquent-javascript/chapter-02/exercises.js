// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(size) {
  let boardString = ''
  for (let i = 0; i < size; i++) {
  for (let j = 0; j < size; j++) {
    if ((i + j) % 2 === 0) {
      boardString += ' ';
} else {
      boardString += '#'
}
}
boardString += '\n'    
}
console.log(boardString);
};

